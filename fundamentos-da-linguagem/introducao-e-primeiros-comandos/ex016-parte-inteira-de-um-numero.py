"""
Parte Inteira de um Número

Objetivo:
Ler um número real e mostrar apenas a parte inteira desse número.
"""

# Entrada de dados
n = float(input("Digite um número: "))

# Cálculo da parte inteira
parte_inteira = int(n)

# Saída de dados
print("-" * 40)
print("O número digitado foi {}".format(n))
print("A parte inteira desse número é {}".format(parte_inteira))
print("-" * 40)
